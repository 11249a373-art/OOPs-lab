#include <iostream>
using namespace std;

int main() {
    int a, b;
    cout << "Enter two numbers: ";
    cin >> a >> b;

    try {
        if (b == 0)
            throw "Division by zero not allowed!";
        else
            cout << "Result = " << (a / b) << endl;
    } catch (const char *msg) {
        cout << "Exception caught: " << msg << endl;
    }

    cout << "Program continues normally..." << endl;
    return 0;
}
